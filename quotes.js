function refreshQuote() {
  const quotes = [
    "Вдохновение — это когда боги целуют тебя, но не говорят тебе об этом.",
    "Неудача — это просто возможность начать снова, но уже более мудро.",
    "Любое препятствие исчезает перед решимостью преодолеть его.",
    "То, что мы достигаем легко, теряет половину своей ценности.",
    "Успех — это идти от поражения к поражению, не теряя энтузиазма.",
    "Если ты хочешь изменить мир, начни с себя."
  ];

  const quote = quotes[Math.floor(Math.random() * quotes.length)];
  document.getElementById('quote').textContent = quote;
}

window.onload = refreshQuote;